// ─── 工具函数 ─────────────────────────────────────────────────────────────────

String removeToneMarks(String pinyin) {
  const Map<String, String> toneMap = {
    'ā': 'a', 'á': 'a', 'ǎ': 'a', 'à': 'a',
    'ē': 'e', 'é': 'e', 'ě': 'e', 'è': 'e',
    'ī': 'i', 'í': 'i', 'ǐ': 'i', 'ì': 'i',
    'ō': 'o', 'ó': 'o', 'ǒ': 'o', 'ò': 'o',
    'ū': 'u', 'ú': 'u', 'ǔ': 'u', 'ù': 'u',
    'ǖ': 'v', 'ǘ': 'v', 'ǚ': 'v', 'ǜ': 'v',
  };
  var result = pinyin.toLowerCase();
  toneMap.forEach((key, value) => result = result.replaceAll(key, value));
  return result;
}

/// 从带声调的拼音中提取声母，如 'shǒu' → 'sh'，无声母返回 null
String? extractInitial(String pinyin) {
  final s = removeToneMarks(pinyin);
  for (final init in ['zh', 'ch', 'sh']) {
    if (s.startsWith(init)) return init;
  }
  for (final init in [
    'b', 'p', 'm', 'f',
    'd', 't', 'n', 'l',
    'g', 'k', 'h',
    'j', 'q', 'x',
    'r', 'z', 'c', 's',
    'y', 'w',
  ]) {
    if (s.startsWith(init)) return init;
  }
  return null;
}

PinyinItem? findInitial(String symbol) {
  try {
    return allInitials.firstWhere((p) => p.symbol == symbol);
  } catch (_) {
    return null;
  }
}

PinyinItem? findFinal(String symbol) {
  try {
    return allFinals.firstWhere((p) => p.symbol == symbol);
  } catch (_) {
    return null;
  }
}
