class PinyinItem {
  final String symbol;
  final String type; // 'initial' | 'final'
  final String example;
  final String examplePinyin;
  final String emoji;
  final List<String> exampleWords;
  final String mnemonic;
  final List<String> blendExamples;
  final List<String> tones; // 韵母专用：['ā','á','ǎ','à']

  const PinyinItem({
    required this.symbol,
    required this.type,
    required this.example,
    required this.examplePinyin,
    required this.emoji,
    required this.exampleWords,
    required this.mnemonic,
    required this.blendExamples,
    this.tones = const [],
  });
}

